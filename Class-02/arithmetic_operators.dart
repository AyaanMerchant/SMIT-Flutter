void main() {
 // Arithmetic Operators (+, -, *, /, %)
num num1 = 23; // Declare and assign the value 23 to num1
num num2 = 3; // Declare and assign the value 3 to num2

num result1 = num1 + num2; // Calculate the sum of num1 and num2 and assign it to result1
num result2 = num1 - num2; // Calculate the difference between num1 and num2 and assign it to result2
num result3 = num1 * num2; // Calculate the product of num1 and num2 and assign it to result3
num result4 = num1 ~/ num2; // Perform integer division of num1 by num2 and assign it to result4
num result5 = num1 % num2; // Calculate the remainder when num1 is divided by num2 and assign it to result5

print("Addition: $result1"); // Print the sum of num1 and num2
print("Subtraction: $result2"); // Print the difference between num1 and num2
print("Multiplication: $result3"); // Print the product of num1 and num2
print("Division: $result4"); // Print the result of integer division of num1 by num2
print("Remainder: $result5"); // Print the remainder when num1 is divided by num2

}
