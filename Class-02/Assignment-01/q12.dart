void main(){
  // Write a program to convert Celsius to Fahrenheit.
  // i.e: Temperature in degrees Fahrenheit (°F) = (Temperature in degrees Celsius (°C) * 9/5) + 32
  
  double celsius = 40; // Declare and assign the temperature in Celsius
  double fahrenheit = (celsius * 9 / 5) + 32; // Convert Celsius to Fahrenheit using the conversion formula

  print('The temperature in Fahrenheit is: $fahrenheit°F'); // Print the converted temperature in Fahrenheit
}
