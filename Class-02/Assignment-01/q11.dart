import 'dart:math';

void main(){
  // Write a program to calculate the square root of a number.
  // i.e: √y = y½

  double number = 40; // Declare and assign the number for which we want to calculate the square root
  double squareRoot = sqrt(number); // Calculate the square root using the sqrt() function from the dart:math library

  print("The square root of $number is $squareRoot"); // Print the result
  
}
