void main() {
//Create two integer variables length and breadth and assign values then check if they are square values or rectangle values.
//example: if both values are equal then it's square otherwise rectangle.

  // Create two integer variables length and breadth and assign values.
  int length = 5; // Assign the value 5 to the variable "length".
  int breadth = 7; // Assign the value 7 to the variable "breadth".

  // Check if length and breadth are square values or rectangle values.
  if (length == breadth) {
    // If the length is equal to the breadth...
    print("It's Square"); // ...print "It's Square".
  } else {
    // If the length is not equal to the breadth...
    print("It's Rectangle"); // ...print "It's Rectangle".
  }
}
